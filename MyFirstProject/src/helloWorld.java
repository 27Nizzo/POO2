class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello, World!");
    }
}

// Comment

//"HelloWorld" -> é o nome de uma classe, e a definição da mesma é o que está dentro das primeiras {}.

// "public static void main(String[] args){...}" -> Este é um método main, ou seja, é o codigo onde o 
// compilador de java começa a executar o código.

//Nota: A função main é o ponto de entrada para qualquer aplicação java, e é mandatório para todo o programa em java.

// "System.out.printl("Hello, World!");" -> é um print statment, ele printa o texto dentro, neste caso "Hello, World!".

